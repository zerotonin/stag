#!/usr/bin/env python
# ╔══════════════════════════════════════════════════════════════════╗
# ║  STAG — embedded.benchmark.avr.gen_inputs                        ║
# ║  « emit test_vectors.h with 64 deterministic Q4.12 inputs »      ║
# ╠══════════════════════════════════════════════════════════════════╣
# ║  The AVR benchmark cycles through 64 precomputed input vectors  ║
# ║  uniform-random over the centroid bounding box ± 0.1.  Same     ║
# ║  fixed seed (= 0) every time so the comparison across MCUs is   ║
# ║  apples-to-apples.                                               ║
# ║                                                                  ║
# ║  64 vectors × 6 features × 2 bytes = 768 bytes PROGMEM.         ║
# ║  Fits comfortably in 32 KB ATmega flash with the centroid table ║
# ║  and the classifier code.                                       ║
# ╚══════════════════════════════════════════════════════════════════╝
"""Emit ``test_vectors.h`` with 64 deterministic Q4.12 input vectors."""

from __future__ import annotations

import argparse
from pathlib import Path

import numpy as np
import pandas as pd

from stag.constants import CLUSTER_RESULTS_DIR, MAXABS_SCALER_CSV
from stag.embedded.export_centroids import (
    DEFAULT_Q_FRAC_BITS,
    QFormat,
    encode_q_format,
)

N_VECTORS_DEFAULT = 64
SEED_DEFAULT      = 0


def parse_args() -> argparse.Namespace:
    p = argparse.ArgumentParser(description=__doc__)
    p.add_argument("--n-vectors", type=int, default=N_VECTORS_DEFAULT,
                   help=f"Number of test vectors (default {N_VECTORS_DEFAULT}; "
                        "must be a power of two for the firmware's i & (N-1) trick).")
    p.add_argument("--seed", type=int, default=SEED_DEFAULT)
    p.add_argument("--out", type=Path,
                   default=Path(__file__).resolve().parent / "test_vectors.h")
    p.add_argument("--centroids", type=Path, default=None,
                   help="k=8 centroids .npy (default: first delPosP in delSize_0).")
    p.add_argument("--arch", choices=("avr", "arm", "msp430", "xtensa"),
                   default="avr",
                   help="Target arch — controls PROGMEM qualifier + includes.")
    return p.parse_args()


def _resolve_centroids(explicit: Path | None) -> Path:
    if explicit is not None:
        return explicit
    matches = sorted(
        (CLUSTER_RESULTS_DIR / "delSize_0" / "k_8" / "centroids").glob("*.npy"),
    )
    if not matches:
        raise SystemExit("k=8 centroids not found locally.")
    return matches[0]


def main() -> None:
    args = parse_args()
    if args.n_vectors & (args.n_vectors - 1):
        raise SystemExit(f"--n-vectors must be a power of two, got {args.n_vectors}")

    centroids = np.load(_resolve_centroids(args.centroids))
    rng = np.random.default_rng(args.seed)
    lo = centroids.min(axis=0) - 0.1
    hi = centroids.max(axis=0) + 0.1
    test_f = lo + (hi - lo) * rng.random((args.n_vectors, centroids.shape[1]))

    q = QFormat(int_bits=16 - DEFAULT_Q_FRAC_BITS, frac_bits=DEFAULT_Q_FRAC_BITS)
    test_q = encode_q_format(test_f, q).astype(np.int16)

    is_avr = args.arch == "avr"
    guard = f"STAG_{args.arch.upper()}_TEST_VECTORS_H"
    qualifier = " PROGMEM" if is_avr else ""

    lines = []
    lines.append("/* Auto-generated by stag.embedded.benchmark.avr.gen_inputs. */")
    lines.append("/* Re-run the script after changing centroids or seed.       */")
    lines.append("")
    lines.append(f"#ifndef {guard}")
    lines.append(f"#define {guard}")
    lines.append("")
    lines.append("#include <stdint.h>")
    if is_avr:
        lines.append("#include <avr/pgmspace.h>")
    lines.append("")
    lines.append(f"#define TEST_VECTORS_N    {args.n_vectors}")
    lines.append(f"#define TEST_VECTORS_D    {test_q.shape[1]}")
    lines.append("")
    lines.append(f"static const int16_t test_inputs_q[{args.n_vectors}]"
                 f"[{test_q.shape[1]}]{qualifier} = {{")
    for row in test_q:
        cells = ", ".join(f"{int(v):>7d}" for v in row)
        lines.append(f"    {{ {cells} }},")
    lines.append("};")
    lines.append("")
    lines.append(f"#endif  /* {guard} */")
    args.out.write_text("\n".join(lines) + "\n")
    print(f"wrote {args.out} ({args.out.stat().st_size:,} bytes)")


if __name__ == "__main__":
    main()
